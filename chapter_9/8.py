def is_palindrome(s):
    """Проверяет, является ли строка палиндромом"""
    return s == s[::-1]

def check_odometer():
    """Ищет число, удовлетворяющее условиям задачи"""
    
    # Перебираем все шестизначные числа
    # Начинаем с 100000, заканчиваем 999996 (чтобы +3 тоже было шестизначным)
    for n in range(100000, 999997):
        # Преобразуем число в строку для работы с отдельными цифрами
        s = str(n)
        
        # УСЛОВИЕ 1: последние 4 цифры - палиндром
        # s[2:] берет цифры с индекса 2 до конца (3-ю,4-ю,5-ю,6-ю цифры)
        if not is_palindrome(s[2:]):
            continue
            
        # УСЛОВИЕ 2: через километр последние 5 цифр - палиндром
        s1 = str(n + 1)
        # s1[1:] берет цифры с индекса 1 до конца (2-ю,3-ю,4-ю,5-ю,6-ю)
        if not is_palindrome(s1[1:]):
            continue
            
        # УСЛОВИЕ 3: еще через километр средние 4 цифры - палиндром
        s2 = str(n + 2)
        # s2[1:5] берет цифры с индекса 1 до 4 (2-ю,3-ю,4-ю,5-ю)
        if not is_palindrome(s2[1:5]):
            continue
            
        # УСЛОВИЕ 4: еще через километр все 6 цифр - палиндром
        s3 = str(n + 3)
        if not is_palindrome(s3):
            continue
            
        # Если все условия выполнены, печатаем результат
        print(f"Начальный пробег: {n}")
        print(f"Через 1 км: {n+1} (последние 5 цифр палиндром)")
        print(f"Через 2 км: {n+2} (средние 4 цифры палиндром)")
        print(f"Через 3 км: {n+3} (все цифры палиндром)")
        print()

# Запускаем поиск
check_odometer()